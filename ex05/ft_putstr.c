#include <unistd.h>

void putchar(void)
{
	write(1, &c, 1);
}

void putstr(void)
{
	int i;
	i = 0;
	while(str[i])
	{
		ft_putchar(str[i]);
		i++;
	}
	ft_putchar('/n');
}
